        <h1 class="primary-heading">Guide to Filing a Criminal Case in the Philippines</h1>
        <p class="paragraph">
            1) <span class="bold"> File a blotter report - </span> The first step in taking legal action is to report the crime to the proper authorities, typically to officials of the barangay where the incident happened, or to the police. The barangay officials or the police will generate a “blotter” which will serve as an important piece of evidence in your case.
            <br><br>    
            2) <span class="bold"> Document Injuries and Damages - </span> If harm was done to your person, it’s important to immediately seek treatment, perhaps even before you report the crime to the authorities.

            No matter how difficult, try to document your injuries. Photographs, along with the doctor’s report, will greatly help prove your case later on. Don’t delay seeking medical attention as any time lapse could negatively affect your case’s credibility.
           
            If you are not the victim but you are acting on the victim’s behalf, make sure the documentation of the injury is as detailed as possible.
           
            If harm was done to your property, photographs of the damage and of the scene are essential.
           
            In all cases, try to find as many witnesses as possible and get their contact information for future use.
            <br><br>
            3) <span class="bold"> Seek a Lawyer’s Help - </span>In seeking justice, you will need the services of qualified attorney, and the earlier you can call on one, the better. Your lawyer will guide and assist you in gathering and preserving evidence.
            <br><br>
            4)<span class="bold"> File a Complaint - </span>  A complaint needs to be filed with the Office of the City Prosecutor (OCP) or Office of the Provincial Prosecutor (OPP), where the incident took place. Your lawyer will draft the Complaint-Affidavit, which will include the details of the incident along with your evidences, your personal information, and information on the offender.
            <br><br>
            5)<span class="bold"> Cooperate with the Preliminary Investigation - </span>The OCP or OPP will then conduct a preliminary investigation to determine if your complaint has sufficient merit, that is, if there’s enough evidence to show that a crime has been committed and that the respondent person is “probably guilty” of the crime.

            In the course of the preliminary investigation, the respondent may issue a Counter-Affidavit to disprove your accusations, to which you may respond with a Reply-Affidavit.
            
            The OCP or OPP will then determine if there’s sufficient ground to pursue a case against the respondent. If they believe there’s none, the case will be dismissed. If they do find sufficient ground, they will file a resolution and information with the proper court.
            <br><br>        
            6)<span class="bold"> Await Judge’s Resolution - </span>  The judge to whose court the case was brought will then investigate the merits of the case and resolve to either dismiss the case or bring it to trial. If the judge decides for a trial, he/she will issue a warrant of arrest for the respondent (this time he/she will be called an accused).

            The judge may allow bail, depending on the nature and gravity of the case, which would grant the accused temporary freedom while the trial is in progress.
            <br><br>
            7) <span class="bold"> Go Through the Trial - </span>Criminal acts are considered offenses against the state, and therefore, criminal cases are handled by state prosecutors or fiscals. However, private lawyers, particularly those you choose, may assist state prosecutors who are often too burdened with numerous cases to give adequate attention to each one.

            Throughout the proceedings, your lawyer will be around to guide you and provide you with the information you need in making your decisions, while working within the system to help you achieve a just resolution.
